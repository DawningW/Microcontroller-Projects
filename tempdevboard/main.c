ds_time_t time;

void uart_send(char c)
{
    SBUF = c;
    while (!TI);
    TI = 0;
}

void uart_print(const char *s)
{
    while (*s)
        uart_send(*s++);
}

void uart_init()
{
    SCON = 0x50;
    TMOD = 0x20;
    TH1 = TL1 = -(FOSC / 12 / 32 / BAUD);
    TR1 = 1;
    ES = 1;
    EA = 1;
}

void main()
{
    char str[16];

    // 将 EX_LVD(P4.6), ALE(P4.5) 和 NC(P4.4) 设置为 I/O 口
    P4SW = 0x70;

    // uart_init();
    // uart_print("STC12C5A60S2\r\n");

    lcd_init();
    ds1302_init();

    if (ds1302_read(DS_SECOND) & 0x80)
    {
        time.year = 22;
        time.month = 9;
        time.day = 2;
        time.dayofweek = 5;
        time.hour = 22;
        time.minute = 0;
        time.second = 0;
        ds1302_set_time(&time);
    }

    while (true)
    {
        lcd_draw_str(0, 0, "Hello World!   ");
        lcd_draw_str(0, 2, "你好，世界！   ");
        delay_ms(3000);
        ds1302_get_time(&time);
        sprintf(str, "%04d/%02bd/%02bd     ", 2000 + time.year, time.month, time.day);
        lcd_draw_str(0, 0, str);
        sprintf(str, "%02bd:%02bd:%02bd       ", time.hour, time.minute, time.second);
        lcd_draw_str(0, 2, str);
        delay_ms(3000);
        lcd_draw_glyph(0, 0, img_hoco12232, 122, 4);
        delay_ms(3000);
    }
}
